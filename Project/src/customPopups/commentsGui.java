    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        
        Statement stmt = null;

        if(commentsArea.getText()!=null){
            try{
                stmt = con.createStatement();
                stmt.executeUpdate("update article set comments = '"+commentsArea.getText()+"' where title = '"+title+"' ");
                stmt.executeUpdate("update article set state = 'rejected' where title = '"+title+"' ");
                rejectLabel.setText("The article has been rejected");
                
            }
            catch(SQLException e){
                System.out.println(e.getMessage());

            }
            finally{

                try{
                    if(stmt!=null){

                        stmt.close();
                    }

                }catch(SQLException e){

                    System.out.println(e.getMessage());

                }

            }

        }
        else{
            JOptionPane.showMessageDialog(null,
                "Please add your comments",
                "Error",
                JOptionPane.WARNING_MESSAGE);

        }
        
        
        
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

